## A person came to India and want to change the currency
n = int(input())
country = input()
if(country == 'Euro'):
    print(n*0.01417)
elif(country == 'British Pound'):
    print(n*0.0100)
elif(country == 'Australian Dollar'):
    print(n*0.02140)
elif(country == "Canadian Dollar"):
    print(n*0.02027)
    
